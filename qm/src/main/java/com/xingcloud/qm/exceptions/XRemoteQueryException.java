package com.xingcloud.qm.exceptions;

public class XRemoteQueryException extends Exception {

    private static final long serialVersionUID = -5431738546451070890L;

    public XRemoteQueryException() {
        super();
    }

    public XRemoteQueryException(String message, Throwable cause) {
        super(message, cause);
    }

    public XRemoteQueryException(String message) {
        super(message);
    }

    public XRemoteQueryException(Throwable cause) {
        super(cause);
    }

}
